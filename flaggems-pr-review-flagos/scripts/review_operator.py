#!/usr/bin/env python3
"""Core review script for FlagGems PR review skill.

Takes PR diff data (from fetch_pr_diff.py or stdin) and runs structural checks,
outputting findings as JSON.

Usage:
    python review_operator.py <pr_number> [--repo owner/repo] [--json] [--strict]
    python review_operator.py --local [--base upstream/master] [--json]
    echo '...' | python review_operator.py --stdin [--json]
"""

