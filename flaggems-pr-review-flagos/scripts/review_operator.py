def verify_environment() -> None:
    """Verify required environment before proceeding."""
    # Check GH_TOKEN (only needed for non-stdin mode, but check early)
    if not os.getenv("GH_TOKEN"):
        print("WARNING: GH_TOKEN not set. PR mode will fail.", file=sys.stderr)
        print("Please run: gh auth login", file=sys.stderr)

    # Check if in git repo or FLAGGEMS_REPO is set
    repo_path = os.getenv("FLAGGEMS_REPO")
    if repo_path:
        repo_path = Path(repo_path).resolve()
        if not (repo_path / ".git").is_dir():
            print(f"ERROR: FLAGGEMS_REPO={repo_path} is not a git repository", file=sys.stderr)
            sys.exit(1)
        try:
            os.chdir(repo_path)
        except OSError as e:
            print(f"ERROR: Cannot cd to FLAGGEMS_REPO={repo_path}: {e}", file=sys.stderr)
            sys.exit(1)
    else:
        # Check if current directory is in a git repo (only warn, not fatal for stdin mode)
        result = subprocess.run(
            ["git", "rev-parse", "--show-toplevel"],
            capture_output=True,
            text=True,
        )
        if result.returncode != 0:
            print("WARNING: Not in a git repository and FLAGGEMS_REPO not set", file=sys.stderr)
            print("PR mode and local mode will fail.", file=sys.stderr)


@dataclass
class Finding:
    severity: str  # "error", "warning", "info"
    rule_id: str
    file: str
    line: int
    message: str
    suggestion: str = ""


