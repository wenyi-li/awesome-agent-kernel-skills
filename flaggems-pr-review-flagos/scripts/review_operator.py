def run_review(data: dict, strict: bool = False) -> list[Finding]:
    """Run all checks on the input data and return findings."""
    operator = data.get("operator")
    files = data.get("files", [])
    commits = data.get("commits", [])

    all_findings: list[Finding] = []

    # Get kernel content for cross-referencing
    kernel_content = ""
    for f in files:
        if categorize_file(f["path"]) == "kernel":
            kernel_content = f.get("content", "")
            break

    # Per-file checks
    for f in files:
        if f.get("status") == "removed":
            continue

        category = categorize_file(f["path"])
        if category == "kernel":
            all_findings.extend(check_kernel(f, operator))
        elif category == "ops_init":
            all_findings.extend(check_ops_init(f, operator))
        elif category == "full_config":
            all_findings.extend(check_full_config(f, operator, kernel_content))
        elif category == "yaml":
            all_findings.extend(check_yaml(f, operator))
        elif category == "test":
            all_findings.extend(check_test(f, operator))
        elif category == "benchmark":
            all_findings.extend(check_benchmark(f, operator))

    # TEST_EXISTS / BENCH_EXISTS
    if operator:
        op_id = operator.lstrip("_")
        file_paths = {f["path"] for f in files}
        has_kernel = any(categorize_file(p) == "kernel" for p in file_paths)
        has_test = any(f"tests/test_{op_id}.py" in p for p in file_paths)
        has_bench = any(f"benchmark/test_{op_id}.py" in p for p in file_paths)

        if has_kernel and not has_test:
            all_findings.append(Finding(
                severity="error",
                rule_id="TEST_EXISTS",
                file=f"tests/test_{op_id}.py",
                line=0,
                message=f"No test file found for operator '{operator}'",
                suggestion=f"Add tests/test_{op_id}.py with accuracy tests.",
            ))
        if has_kernel and not has_bench:
            all_findings.append(Finding(
                severity="error",
                rule_id="BENCH_EXISTS",
                file=f"benchmark/test_{op_id}.py",
                line=0,
                message=f"No benchmark file found for operator '{operator}'",
                suggestion=f"Add benchmark/test_{op_id}.py with performance benchmarks.",
            ))

    # Cross-file checks
    all_findings.extend(check_cross_file(files, commits, operator))
    all_findings.extend(check_yaml_consistency(files, operator))

    # In strict mode, promote warnings to errors
    if strict:
        for f in all_findings:
            if f.severity == "warning":
                f.severity = "error"

    return all_findings


def format_terminal(findings: list[Finding]) -> str:
    """Format findings as a colored terminal table."""
    if not findings:
        return "\033[32m No issues found.\033[0m\n"

    severity_colors = {
        "error": "\033[31m",    # red
        "warning": "\033[33m",  # yellow
        "info": "\033[36m",     # cyan
    }
    reset = "\033[0m"

    lines: list[str] = []
    lines.append(f"\n{'Severity':<10} {'Rule':<22} {'File':<45} {'Line':<6} Message")
    lines.append("-" * 120)

    for f in findings:
        color = severity_colors.get(f.severity, "")
        sev = f"{color}{f.severity.upper():<10}{reset}"
        file_short = f.file if len(f.file) <= 44 else "..." + f.file[-41:]
        lines.append(f"{sev} {f.rule_id:<22} {file_short:<45} {f.line:<6} {f.message}")
        if f.suggestion:
            lines.append(f"{'':>10} {'':>22} {'':>45} {'':>6} -> {f.suggestion}")

    error_count = sum(1 for f in findings if f.severity == "error")
    warn_count = sum(1 for f in findings if f.severity == "warning")
    info_count = sum(1 for f in findings if f.severity == "info")
    lines.append("")
    lines.append(
        f"Summary: {error_count} error(s), {warn_count} warning(s), {info_count} info"
    )

    return "\n".join(lines) + "\n"


def main() -> None:
    # Verify environment first (warnings only, not fatal for stdin mode)
    verify_environment()

    parser = argparse.ArgumentParser(
        description="FlagGems PR review: structural checks on operator PRs.",
    )
    parser.add_argument(
        "pr_number",
        nargs="?",
        type=int,
        help="Pull request number (fetches data via fetch_pr_diff.py)",
    )
    parser.add_argument(
        "--repo",
        type=str,
        default=None,
        help="Repository in owner/repo format",
    )
    parser.add_argument(
        "--local",
        action="store_true",
        help="Use local mode (passed to fetch_pr_diff.py)",
    )
    parser.add_argument(
        "--base",
        type=str,
        default="upstream/master",
        help="Base ref for local mode (default: upstream/master)",
    )
    parser.add_argument(
        "--stdin",
        action="store_true",
        help="Read JSON input from stdin instead of calling fetch_pr_diff.py",
    )
    parser.add_argument(
        "--json",
        action="store_true",
        help="Output findings as JSON (default for stdout when piped)",
    )
    parser.add_argument(
        "--strict",
        action="store_true",
        help="Treat warnings as errors",
    )

    args = parser.parse_args()

    # Get input data
    if args.stdin:
        data = json.load(sys.stdin)
    else:
        # Build command to invoke fetch_pr_diff.py
        script_dir = Path(__file__).resolve().parent
        fetch_script = script_dir / "fetch_pr_diff.py"

        cmd = [sys.executable, str(fetch_script)]
        if args.local:
            cmd.append("--local")
            cmd.extend(["--base", args.base])
        elif args.pr_number is not None:
            cmd.append(str(args.pr_number))
            if args.repo:
                cmd.extend(["--repo", args.repo])
        else:
            parser.error("Provide a PR number, use --local, or use --stdin")

        result = subprocess.run(cmd, capture_output=True, text=True)
        if result.returncode != 0:
            print(f"Error fetching PR data: {result.stderr.strip()}", file=sys.stderr)
            sys.exit(1)
        try:
            data = json.loads(result.stdout)
        except json.JSONDecodeError as e:
            print(f"Error: fetch_pr_diff.py returned invalid JSON: {e}", file=sys.stderr)
            print(f"stdout: {result.stdout[:500]}", file=sys.stderr)
            sys.exit(1)

    # Run review
    findings = run_review(data, strict=args.strict)

    # Output
    use_json = args.json or not sys.stdout.isatty()
    if use_json:
        output = {
            "operator": data.get("operator"),
            "findings": [asdict(f) for f in findings]
        }
        json.dump(output, sys.stdout, indent=2, ensure_ascii=False)
        print()
    else:
        sys.stdout.write(format_terminal(findings))

    # Exit code: always 0 for normal operation (findings are in the JSON output)
    # Only non-zero exit codes come from actual script errors (e.g., fetch failure above)


if __name__ == "__main__":
    main()
