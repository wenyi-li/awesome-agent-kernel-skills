def parse_added_lines(patch: str) -> list[tuple[int, str]]:
    """Extract (line_number, content) for added lines from a unified diff patch."""
    lines: list[tuple[int, str]] = []
    current_line = 0
    for raw in patch.splitlines():
        if raw.startswith("@@"):
            # Parse hunk header: @@ -a,b +c,d @@
            m = re.search(r"\+(\d+)", raw)
            if m:
                current_line = int(m.group(1))
            continue
        if raw.startswith("+") and not raw.startswith("+++"):
            lines.append((current_line, raw[1:]))
            current_line += 1
        elif raw.startswith("-") and not raw.startswith("---"):
            # Removed line, doesn't advance the new-file line counter
            continue
        else:
            current_line += 1
    return lines


def safe_ast_parse(content: str, filename: str = "<unknown>") -> ast.Module | None:
    """Try to parse Python source; return None on failure."""
    try:
        return ast.parse(content, filename=filename)
    except SyntaxError:
        return None


