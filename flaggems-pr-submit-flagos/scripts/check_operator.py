    def check_anti_hack_layer2(self):
        """Layer 2: 禁用 triton.jit 后重跑，验证 kernel 确实在做 Triton 计算。"""
        section("Anti-hack Layer 2 (dual execution)")
        if not os.path.isfile(self.kernel_path):
            warn("kernel 文件不存在，跳过")
            return

        anti_hack_path = os.environ.get(
            "ANTI_HACK_SCRIPT", "/workspace/scripts/anti_hack.py"
        )
        if not os.path.isfile(anti_hack_path):
            warn(f"anti_hack.py 不存在: {anti_hack_path}，跳过 Layer 2")
            return

        try:
            import torch
            if not torch.cuda.is_available():
                warn("无 GPU 可用，跳过 Layer 2 dual-execution 检查")
                return

            spec = importlib.util.spec_from_file_location("anti_hack", anti_hack_path)
            anti_hack = importlib.util.module_from_spec(spec)
            spec.loader.exec_module(anti_hack)

            sys.path.insert(0, os.path.join(self.repo_dir, "src"))
            op_module = importlib.import_module(f"flag_gems.ops.{self.op_name}")

            wrapper_func = getattr(op_module, self.op_name, None)
            if wrapper_func is None:
                warn(f"无法导入 wrapper 函数 '{self.op_name}'，跳过 Layer 2")
                return

            # 构造小 tensor 输入用于 dual-execution 检查
            import inspect
            sig = inspect.signature(wrapper_func)
            params = list(sig.parameters.keys())

            # 尝试用小 tensor 调用
            test_input = torch.randn(2, 3, 4, 4, device="cuda", dtype=torch.float32)
            try:
                # 通用调用：大部分 op 第一个参数是 input tensor
                if len(params) >= 2:
                    # 尝试获取第二个参数的默认值
                    second_param = sig.parameters[params[1]]
                    if second_param.default is not inspect.Parameter.empty:
                        kwargs = {params[0]: test_input}
                    else:
                        kwargs = {params[0]: test_input}
                else:
                    kwargs = {params[0]: test_input}

                is_hack, reason = anti_hack.dual_execution_check(
                    lambda **kw: wrapper_func(**kw), kwargs, rtol=1e-3, atol=1e-3
                )

                if is_hack:
                    self.errors.append(
                        f"anti-hack Layer 2 失败: kernel 禁用 triton.jit 后结果不变，"
                        f"说明未使用 Triton 进行实际计算。{reason}"
                    )
                    fail(f"Layer 2 失败: kernel 未使用 Triton 计算!\n    {reason}")
                else:
                    ok("anti-hack Layer 2 通过（kernel 确实依赖 Triton 计算）")
            except Exception as e:
                # 如果调用失败（参数不匹配等），不阻塞
                warn(f"Layer 2 跳过（调用异常: {type(e).__name__}: {e}）")
        except ImportError as e:
            warn(f"Layer 2 跳过（导入失败: {e}）")
        except Exception as e:
            warn(f"Layer 2 检查异常: {e}")
        finally:
            if os.path.join(self.repo_dir, "src") in sys.path:
                sys.path.remove(os.path.join(self.repo_dir, "src"))

