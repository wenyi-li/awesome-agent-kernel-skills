    def check_use_tl_extra_stubs(self):
        section("@use_tl_extra 桩函数检查")
        if not os.path.isfile(self.kernel_path):
            return

        with open(self.kernel_path) as f:
            content = f.read()

        # 匹配 @use_tl_extra 装饰的函数（可能跟 @triton.jit 一起）
