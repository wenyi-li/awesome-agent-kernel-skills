    def _check_private_torch_api(self, content, file_label):
        """检查测试/benchmark 中是否直接调用了 torch._ 私有 API。
        私有 API 在不同 PyTorch 版本中可能不存在，应使用公开 API。
        """
        private_calls = re.findall(r"torch\.(_[a-z]\w*)\(", content)
        # 过滤掉 torch._C 等模块级调用和常见合法用法
        skip = {"_C", "_jit_internal", "_utils", "_dynamo"}
        private_calls = [c for c in private_calls if c not in skip]
        if private_calls:
            unique = sorted(set(private_calls))
            self.errors.append(
                f"{file_label}中直接调用了 torch 私有 API: {', '.join('torch.' + c for c in unique)}"
                f"。私有 API 在不同 PyTorch 版本中可能不存在，应使用对应的公开 API"
            )
            fail(
                f"调用了私有 API: {', '.join('torch.' + c for c in unique)}"
                f" → 应替换为公开 API"
            )
        else:
            ok(f"未调用 torch 私有 API")

    def _check_dtype_hardcoding(self, content, file_label, correct_const):
        """检查 dtype 是否使用了硬编码列表而非统一常量。
        如果硬编码行的上一行有注释说明原因（如 CUDA 不支持某精度），降级为 ok。
        多 dtype 硬编码列表（如 [torch.float32, torch.float16, torch.bfloat16]）无注释报 error。
        """
        # 多 dtype 硬编码模式（parametrize 中常见）
        multi_dtype_patterns = [
            r"\[torch\.float32,\s*torch\.float16,\s*torch\.bfloat16\]",
            r"\[torch\.float16,\s*torch\.float32,\s*torch\.bfloat16\]",
            r"\[torch\.bfloat16,\s*torch\.float16,\s*torch\.float32\]",
            r"\[torch\.float32,\s*torch\.float16\]",
            r"\[torch\.float32,\s*torch\.float64\]",
            r"\[torch\.float16,\s*torch\.float32\]",
        ]
        # 单 dtype 硬编码模式
        single_dtype_patterns = [
            r"\[torch\.float32\]",
            r"\[torch\.float64\]",
            r"\[torch\.float16\]",
            r"\[torch\.bfloat16\]",
        ]
        lines = content.split("\n")

        def _has_comment_above(line_no):
            if line_no > 0:
                prev_line = lines[line_no - 1].strip()
                if prev_line.startswith("#"):
                    return True
            return False

        # 检查多 dtype 硬编码（强制 error）
        for pattern in multi_dtype_patterns:
            match = re.search(pattern, content)
            if match:
                match_pos = match.start()
                line_no = content[:match_pos].count("\n")
                if _has_comment_above(line_no):
                    ok(f"dtype 硬编码 {match.group()}（有注释说明原因，允许）")
                    return
                self.errors.append(
                    f"{file_label}中 dtype 使用了硬编码 {match.group()}，应使用 {correct_const}"
                    f"（如因 CUDA 限制无法使用全部精度，需在上一行加 # 注释说明原因）"
                )
                fail(f"dtype 硬编码: {match.group()} → 应使用 {correct_const}（或加注释说明原因）")
                return

        # 检查单 dtype 硬编码
        for pattern in single_dtype_patterns:
            match = re.search(pattern, content)
            if match:
                match_pos = match.start()
                line_no = content[:match_pos].count("\n")
                if _has_comment_above(line_no):
                    ok(f"dtype 硬编码 {match.group()}（有注释说明原因，允许）")
                    return
                self.errors.append(
                    f"{file_label}中 dtype 使用了硬编码 {match.group()}，应使用 {correct_const}"
                    f"（需在上一行加 # 注释说明原因）"
                )
                fail(f"dtype 硬编码: {match.group()} → 应使用 {correct_const}（或加注释说明原因）")
                return

        if correct_const.split(".")[-1] in content or "FLOAT_DTYPES" in content or "INT_DTYPES" in content:
            ok("dtype 使用统一常量")
        else:
            self.errors.append(f"{file_label}中未检测到标准 dtype 常量，也未找到带注释的硬编码 dtype")
            fail(f"未检测到 {correct_const} 且无带注释的 dtype 硬编码")

