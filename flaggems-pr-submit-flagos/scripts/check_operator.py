    def check_yaml_config_consistency(self):
        """yaml 'for' 列表 vs _FULL_CONFIG 注册名交叉验证。"""
        section("YAML/Config 一致性检查")

        # 提取 yaml for 列表
        yaml_for = set()
        if os.path.isfile(self.yaml_path):
            with open(self.yaml_path) as f:
                try:
                    data = yaml.safe_load(f)
                except yaml.YAMLError:
                    warn("yaml 解析失败，跳过一致性检查")
                    return
            for op in data.get("ops", []):
                if op.get("id") == self.op_id:
                    yaml_for = set(op.get("for", []))
                    break

        if not yaml_for:
            warn("yaml 中未找到 for 列表，跳过一致性检查")
            return

        # 提取 _FULL_CONFIG 中该算子的注册 aten name
        config_aten = set()
        if os.path.isfile(self.top_init_path):
            with open(self.top_init_path) as f:
                init_content = f.read()
            config_match = re.search(
                r"_FULL_CONFIG\s*=\s*\((.+?)\)\s*(?:\n\n|$)", init_content, re.DOTALL
            )
            if config_match:
                config_text = config_match.group(1)
                # 匹配所有 ("aten_name", func_name, ...) 条目
                for m in re.finditer(
                    r'\(\s*["\']([^"\']+)["\']\s*,\s*(\w+)', config_text
                ):
                    aten_name, func_name = m.groups()
                    # 该算子的注册：func_name 匹配 op_name 或 op_id（含 inplace 后缀）
                    base_func = func_name.rstrip("_")
                    if base_func == self.op_name or base_func == self.op_id or func_name == self.op_name:
                        config_aten.add(aten_name)

        if not config_aten:
            warn("_FULL_CONFIG 中未找到该算子注册，跳过一致性检查")
            return

        # 比较
        # yaml for 中的名字用于 aten dispatch，config 中也是 aten name
        {name.split(".")[0] for name in yaml_for}
        {name.split(".")[0] for name in config_aten}

        yaml_only = yaml_for - config_aten
        config_only = config_aten - yaml_for

        if yaml_only:
            self.warnings.append(f"yaml 'for' 包含但 _FULL_CONFIG 未注册: {yaml_only}")
            warn(f"yaml 有但 config 无: {yaml_only}")
        if config_only:
            self.warnings.append(f"_FULL_CONFIG 注册但 yaml 'for' 未包含: {config_only}")
            warn(f"config 有但 yaml 无: {config_only}")
        if not yaml_only and not config_only:
            ok(f"yaml 'for' 与 _FULL_CONFIG 一致: {yaml_for}")

    def check_fallback_recursion(self):
        """检测 kernel 中是否有 torch.<op_name>() 调用导致无限递归 dispatch。"""
        section("Fallback 递归检测")
        if not os.path.isfile(self.kernel_path):
            warn("kernel 文件不存在，跳过")
            return

        with open(self.kernel_path) as f:
            content = f.read()

        # 收集该算子可能的 torch.xxx 调用形式
        op_variants = {self.op_name, self.op_id}
        if self.op_name.endswith("_"):
            op_variants.add(self.op_name.rstrip("_"))

        dangerous_calls = []
        for line_no, line in enumerate(content.split("\n"), 1):
            code_part = line.split("#")[0]
            if "torch.ops.aten." in code_part or "torch.ops." in code_part:
                continue
            for variant in op_variants:
                pattern = rf"torch\.{re.escape(variant)}\s*\("
                if re.search(pattern, code_part):
                    dangerous_calls.append((line_no, variant, line.strip()))

        if dangerous_calls:
            for line_no, variant, line_text in dangerous_calls:
                self.errors.append(
                    f"kernel 第 {line_no} 行调用了 torch.{variant}()，"
                    f"会触发无限递归 dispatch。应使用 torch.ops.aten.{variant}() 或纯 Triton 实现"
                )
                fail(f"第 {line_no} 行: torch.{variant}() → 无限递归! 应改为 torch.ops.aten.{variant}()")
        else:
            ok("未检测到递归 dispatch 风险")

