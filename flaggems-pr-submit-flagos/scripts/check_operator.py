#!/usr/bin/env python3
"""FlagGems 算子 PR 提交前自动化检查脚本。

用法:
    python check_operator.py <operator_name> [--repo-dir /path/to/FlagGems]

检查项:
    1. kernel 文件存在且内容合规
    2. __init__.py 注册（ops 和顶层）
    3. operators.yaml 条目（含重复检测、下划线命名）
    4. 独立测试文件（含 dtype 硬编码检测、mark 命名）
    5. 独立 benchmark 文件（含 dtype 硬编码检测、mark 命名）
    6. 代码质量（重复函数、print、unused import 等）
    7. _FULL_CONFIG 映射目标验证
    8. 命名规范（下划线前缀算子的 mark/id 规则）
    9. KernelGen 首行注释
   10. gems_assert_close 禁止 rtol 参数
   11. Co-Authored-By 检查
   12. inplace mark 守卫
   13. 测试函数命名规范
   14. 显式 .cpu()/.to(torch.float64) 检测
   15. yaml/config 注册一致性
   16. anti-hack Layer 1 AST 扫描
   17. 单算子 PR 验证
   18. @use_tl_extra 桩函数检查
   19. benchmark case 数量统计
"""

import argparse
import ast
import importlib.util
import os
import re
import subprocess
import sys

import yaml


class Colors:
    OK = "\033[92m"
    WARN = "\033[93m"
    FAIL = "\033[91m"
    BOLD = "\033[1m"
    END = "\033[0m"


def ok(msg):
    print(f"  {Colors.OK}✓{Colors.END} {msg}")


def warn(msg):
    print(f"  {Colors.WARN}⚠{Colors.END} {msg}")


def fail(msg):
    print(f"  {Colors.FAIL}✗{Colors.END} {msg}")


def section(msg):
    print(f"\n{Colors.BOLD}[{msg}]{Colors.END}")


class OperatorChecker:
    def __init__(self, op_name, repo_dir):
        self.op_name = op_name
        self.repo_dir = repo_dir
        self.errors = []
        self.warnings = []

        # 下划线前缀的算子，mark 和 yaml id 需要去掉前导下划线
        self.op_id = op_name.lstrip("_")
        self.has_leading_underscore = op_name.startswith("_")

        self.kernel_path = os.path.join(repo_dir, "src/flag_gems/ops", f"{op_name}.py")
        self.ops_init_path = os.path.join(repo_dir, "src/flag_gems/ops/__init__.py")
        self.top_init_path = os.path.join(repo_dir, "src/flag_gems/__init__.py")
        self.yaml_path = os.path.join(repo_dir, "conf/operators.yaml")
        # 测试/benchmark 文件名使用 op_id（去掉前导下划线）
        self.test_path = os.path.join(repo_dir, "tests", f"test_{self.op_id}.py")
        self.bench_path = os.path.join(repo_dir, "benchmark", f"test_{self.op_id}.py")
        # 旧版错误路径（用于检测）
        if self.has_leading_underscore:
            self.wrong_test_path = os.path.join(repo_dir, "tests", f"test_{op_name}.py")
            self.wrong_bench_path = os.path.join(repo_dir, "benchmark", f"test_{op_name}.py")
        else:
            self.wrong_test_path = None
            self.wrong_bench_path = None

    def run_all(self):
        if self.has_leading_underscore:
            section("命名规则提示")
            ok(f"检测到下划线前缀算子: {self.op_name}")
            ok(f"  文件名/函数名/import/_FULL_CONFIG: 保留 {self.op_name}")
            ok(f"  pytest mark / yaml id: 使用 {self.op_id}（去掉前导下划线）")

        self.check_kernel()
        self.check_ops_init()
        self.check_top_init()
        self.check_operators_yaml()
        self.check_test_file()
        self.check_benchmark_file()
        self.check_code_quality()
        self.check_upstream_conflict()
        self.check_git_commit_message()
        self.check_inplace_mark()
        self.check_yaml_config_consistency()
        self.check_fallback_recursion()
        self.check_anti_hack()
        self.check_anti_hack_layer2()
        self.check_single_op_per_pr()
        self.check_use_tl_extra_stubs()
        self.check_hardcoded_sizes()
        self.check_benchmark_case_count()
        self.check_alias_wrappers()
        self.check_nan_handling()
        self.check_worktree_diff()
        self.check_dtype_assert_in_wrapper()
        self.print_summary()
        return len(self.errors) == 0

    def check_kernel(self):
        section("Kernel 文件检查")
        if not os.path.isfile(self.kernel_path):
            self.errors.append(f"kernel 文件不存在: {self.kernel_path}")
            fail(f"文件不存在: {self.kernel_path}")
            return

        ok(f"文件存在: {self.kernel_path}")

        with open(self.kernel_path) as f:
            content = f.read()

        # KernelGen 首行注释检查
        first_line = content.split("\n")[0] if content else ""
        if first_line.startswith("# Generated by KernelGen"):
            ok("首行包含 KernelGen 注释")
        else:
            self.errors.append("kernel 文件首行必须是 '# Generated by KernelGen: ...'")
            fail("首行缺少 KernelGen 注释")

        if "import logging" not in content or "logger" not in content:
            self.warnings.append("kernel 文件未使用 logging 模块")
            warn("未使用 logging 模块（建议用 logger = logging.getLogger(__name__)）")
        else:
            ok("使用了 logging 模块")

        if "print(" in content:
            self.errors.append("kernel 文件中使用了 print()")
            fail("包含 print() 调用 — 应使用 logger.debug()")
        else:
            ok("无 print() 调用")

        self._check_duplicate_functions(content, self.kernel_path)

        try:
            tree = ast.parse(content)
        except SyntaxError as e:
            self.errors.append(f"kernel 文件语法错误: {e}")
            fail(f"Python 语法错误: {e}")
            return

        # 检查未使用的 import torch
        has_torch_import = False
        torch_used_in_annotation_or_call = False
        for node in ast.walk(tree):
            if isinstance(node, ast.Import):
                for alias in node.names:
                    if alias.name == "torch":
                        has_torch_import = True
            if isinstance(node, ast.Attribute):
                if isinstance(node.value, ast.Name) and node.value.id == "torch":
                    torch_used_in_annotation_or_call = True
            if isinstance(node, ast.Name) and node.id == "torch":
                torch_used_in_annotation_or_call = True

        if has_torch_import and not torch_used_in_annotation_or_call:
            self.warnings.append("kernel 文件 import torch 但未实际使用（flake8 F401）")
            warn("import torch 但未使用 — flake8 会报 F401，提交前需删除")
        elif has_torch_import:
            ok("import torch 已使用")
        else:
            ok("无多余的 torch import")

        # 收集导出函数和 jit 函数信息
        exported = [
            n.name for n in ast.walk(tree)
            if isinstance(n, ast.FunctionDef)
            and not any(
                (isinstance(d, ast.Attribute) and d.attr == "jit")
                or (isinstance(d, ast.Name) and d.id == "jit")
                for d in n.decorator_list
            )
        ]
        jit_funcs = [
            n.name for n in ast.walk(tree)
            if isinstance(n, ast.FunctionDef) and any(
                (isinstance(d, ast.Attribute) and d.attr == "jit")
                or (isinstance(d, ast.Name) and d.id == "jit")
                for d in n.decorator_list
            )
        ]

        if exported:
            ok(f"导出函数: {', '.join(exported)}")
        else:
            self.warnings.append("未找到非 jit 导出函数")
            warn("未找到公开的非 @triton.jit 函数")

        if jit_funcs:
            ok(f"Triton kernel: {', '.join(jit_funcs)}")

        # 保存供 _FULL_CONFIG 验证使用
        self._kernel_exported = exported
        self._kernel_jit_funcs = jit_funcs

        # _out 变体检测：如果导出了 xxx_out 函数，test/benchmark 中必须覆盖
        out_funcs = [f for f in exported if f.endswith("_out")]
        if out_funcs:
            for out_func in out_funcs:
                self._check_out_variant_coverage(out_func)

    def check_ops_init(self):
        section("ops/__init__.py 注册检查")
        if not os.path.isfile(self.ops_init_path):
            self.errors.append("ops/__init__.py 不存在")
            fail("文件不存在")
            return

        with open(self.ops_init_path) as f:
            content = f.read()

        import_pattern = rf"from flag_gems\.ops\.{re.escape(self.op_name)} import"
        if re.search(import_pattern, content):
            ok(f"找到 import: from flag_gems.ops.{self.op_name} import ...")

            try:
                tree_init = ast.parse(content)
                imported_names = []
                for node in ast.walk(tree_init):
                    if isinstance(node, ast.ImportFrom) and node.module == f"flag_gems.ops.{self.op_name}":
                        for alias in node.names:
                            imported_names.append(alias.asname or alias.name)
                if imported_names:
                    ok(f"  导入名称: {', '.join(imported_names)}")
                    # 检查是否导出了 triton kernel 函数（不应该）
                    if hasattr(self, "_kernel_jit_funcs"):
                        leaked = set(imported_names) & set(self._kernel_jit_funcs)
                        if leaked:
                            self.errors.append(f"ops/__init__.py 不应导出 triton kernel 函数: {', '.join(leaked)}")
                            fail(f"  不应导出 triton kernel: {', '.join(leaked)}")
            except SyntaxError:
                pass
        else:
            self.errors.append(f"ops/__init__.py 中缺少 from flag_gems.ops.{self.op_name} import ...")
            fail(f"缺少 import 行: from flag_gems.ops.{self.op_name} import ...")

        all_match = re.search(r"__all__\s*=\s*\[([^\]]+)\]", content, re.DOTALL)
        if all_match:
            all_content = all_match.group(1)
            if f'"{self.op_name}"' in all_content or f"'{self.op_name}'" in all_content:
                ok(f'__all__ 中包含 "{self.op_name}"')
            else:
                self.errors.append(f'__all__ 中缺少 "{self.op_name}"')
                fail(f'__all__ 中缺少 "{self.op_name}"')
        else:
            self.warnings.append("未找到 __all__ 定义")
            warn("未找到 __all__ 列表")

        self._check_alphabetical_order_init(content)

    def check_top_init(self):
        section("顶层 __init__.py (_FULL_CONFIG) 检查")
        if not os.path.isfile(self.top_init_path):
            self.errors.append("顶层 __init__.py 不存在")
            fail("文件不存在")
            return

        with open(self.top_init_path) as f:
            content = f.read()

        config_match = re.search(r"_FULL_CONFIG\s*=\s*\((.+?)\)\s*\n\n", content, re.DOTALL)
        if not config_match:
            config_match = re.search(r"_FULL_CONFIG\s*=\s*\((.+?)\)\s*$", content, re.DOTALL)

        if not config_match:
            self.warnings.append("未找到 _FULL_CONFIG 定义")
            warn("未找到 _FULL_CONFIG")
            return

        config_text = config_match.group(1)

        # 查找包含 op_name 的注册条目
        found = False
        pattern = rf'\(\s*["\'](?:[^"\']*\.)?_?{re.escape(self.op_name)}(?:\.[^"\']*)?["\']\s*,\s*(\w+)'
        matches = re.findall(pattern, config_text)
        if matches:
            found = True
            for target_func in matches:
                ok(f'_FULL_CONFIG 中找到 "{self.op_name}" → {target_func}')
                # 验证映射目标不是 triton kernel
                if hasattr(self, "_kernel_jit_funcs") and target_func in self._kernel_jit_funcs:
                    self.errors.append(
                        f"_FULL_CONFIG 中 {self.op_name} 映射到了 triton kernel '{target_func}'"
                        f"，应映射到 wrapper 函数 '{self.op_name}'"
                    )
                    fail(f"  映射目标 '{target_func}' 是 triton kernel! 应映射到 wrapper 函数")

        if not found:
            # 尝试更宽松的匹配
            if f'"{self.op_name}"' in config_text or f"'{self.op_name}'" in config_text:
                found = True
                ok(f'_FULL_CONFIG 中找到 "{self.op_name}" 注册')

        if not found:
            self.errors.append(f"_FULL_CONFIG 中缺少 {self.op_name} 注册")
            fail(f"_FULL_CONFIG 中未找到 {self.op_name} 的注册条目")

        # aten name 格式验证: special.* 算子必须用 "special.xxx" 而非 "special_xxx"
        if config_match:
            config_text_full = config_match.group(1)
            bad_special = re.findall(
                r'\(\s*"(special_\w+)"\s*,', config_text_full
            )
            # 只报告与当前算子相关的
            for bad_name in bad_special:
                base = bad_name.replace("special_", "")
                if base == self.op_id or base == self.op_name or self.op_name.startswith("special"):
                    correct_name = bad_name.replace("special_", "special.", 1)
                    self.errors.append(
                        f'_FULL_CONFIG 中 aten name "{bad_name}" 格式错误，'
                        f'special.* 类算子应使用 "{correct_name}"'
                    )
                    fail(f'aten name 格式错误: "{bad_name}" → 应为 "{correct_name}"')

    def check_operators_yaml(self):
        section("conf/operators.yaml 检查")
        if not os.path.isfile(self.yaml_path):
            self.errors.append("operators.yaml 不存在")
            fail("文件不存在")
            return

        with open(self.yaml_path) as f:
            try:
                data = yaml.safe_load(f)
            except yaml.YAMLError as e:
                self.errors.append(f"operators.yaml 解析失败: {e}")
                fail(f"YAML 解析失败: {e}")
                return

        ops = data.get("ops", [])

        # 检查重复条目
        id_counts = {}
        for op in ops:
            op_id = op.get("id", "")
            id_counts[op_id] = id_counts.get(op_id, 0) + 1

        # 查找匹配的条目（使用 op_id，即去掉前导下划线的版本）
        found = None
        for op in ops:
            if op.get("id") == self.op_id:
                found = op
                break

        if not found:
            self.errors.append(f"operators.yaml 中缺少 id: {self.op_id}")
            fail(f"缺少条目 id: {self.op_id}")
            if self.has_leading_underscore:
                # 检查是否错误地用了带下划线的 id
                for op in ops:
                    if op.get("id") == self.op_name:
                        self.errors.append(
                            f"operators.yaml 中 id 使用了 '{self.op_name}'（带下划线），"
                            f"应改为 '{self.op_id}'（去掉前导下划线）"
                        )
                        fail(f"  发现错误的 id: '{self.op_name}' → 应为 '{self.op_id}'")
                        break
            return

        ok(f"找到条目 id: {self.op_id}")

        # 检查重复
        if id_counts.get(self.op_id, 0) > 1:
            self.errors.append(
                f"operators.yaml 中 id '{self.op_id}' 出现了 {id_counts[self.op_id]} 次（应只有 1 次）"
            )
            fail(f"  id '{self.op_id}' 重复了 {id_counts[self.op_id]} 次!")
        else:
            ok("  无重复条目")

        required_fields = ["id", "description", "for", "labels", "kind", "stages"]
        for field in required_fields:
            if field in found:
                ok(f"  字段 {field}: ✓")
            else:
                self.errors.append(f"operators.yaml 条目缺少字段: {field}")
                fail(f"  缺少字段: {field}")

        # 检查 description 不是模板占位符
        desc = found.get("description", "")
        if desc and ("Computes the " + self.op_name + " operation" in desc
                     or "Computes the " + self.op_id + " operation" in desc):
            self.warnings.append("operators.yaml description 是模板占位符，应写实际描述")
            warn("  description 是模板占位符 — 应从 PyTorch 文档提取实际描述")

        labels = found.get("labels", [])
        if "KernelGen" not in labels:
            self.warnings.append("operators.yaml labels 中缺少 KernelGen")
            warn("  labels 中缺少 'KernelGen'（新增算子应包含）")
        else:
            ok("  labels 包含 'KernelGen'")

        if "aten" not in labels:
            self.warnings.append("operators.yaml labels 中缺少 aten 标签")
            warn("  labels 中缺少 'aten'")

        # 检查 for 字段是否包含正确的 aten op name（保留原始下划线）
        for_list = found.get("for", [])
        if for_list and self.op_name not in for_list:
            self.warnings.append(
                f"operators.yaml for 字段中未包含 '{self.op_name}'（aten op 原始名称应保留下划线）"
            )
            warn(f"  for 字段建议包含 '{self.op_name}'")

    def check_test_file(self):
        section("测试文件检查")
        if not os.path.isfile(self.test_path):
            # 检查是否用了错误的文件名（带下划线）
            if self.wrong_test_path and os.path.isfile(self.wrong_test_path):
                self.errors.append(
                    f"测试文件名错误: test_{self.op_name}.py → 应为 test_{self.op_id}.py（去掉前导下划线）"
                )
                fail(f"文件名错误: test_{self.op_name}.py → 应为 test_{self.op_id}.py")
            else:
                self.errors.append(f"测试文件不存在: {self.test_path}")
                fail(f"文件不存在: tests/test_{self.op_id}.py")
            return

        ok(f"文件存在: tests/test_{self.op_name}.py")

        with open(self.test_path) as f:
            content = f.read()

        # pytest mark 检查（使用去掉下划线的 op_id）
        correct_mark = f"@pytest.mark.{self.op_id}"
        wrong_mark = f"@pytest.mark.{self.op_name}" if self.has_leading_underscore else None

        if correct_mark in content:
            ok(f"pytest mark 正确: {correct_mark}")
        elif wrong_mark and wrong_mark in content:
            self.errors.append(
                f"测试文件 pytest mark 使用了 '{wrong_mark}'（带下划线），应改为 '{correct_mark}'"
            )
            fail(f"pytest mark 错误: {wrong_mark} → 应为 {correct_mark}")
        else:
            self.errors.append(f"测试文件缺少 {correct_mark}")
            fail(f"缺少 pytest mark: {correct_mark}")

        if "from . import accuracy_utils as utils" in content:
            ok("使用相对导入 accuracy_utils")
        elif "from tests import accuracy_utils" in content:
            self.errors.append("测试文件使用了绝对导入而非相对导入")
            fail("应使用 'from . import accuracy_utils as utils'（相对导入）")
        else:
            self.warnings.append("测试文件中未找到 accuracy_utils 导入")
            warn("未找到 accuracy_utils 导入")

        if "flag_gems.use_gems()" in content:
            ok("使用 flag_gems.use_gems() 上下文")
        else:
            self.warnings.append("测试文件中未使用 flag_gems.use_gems()")
            warn("未找到 flag_gems.use_gems() — 确认测试方式是否正确")

        if "gems_assert_close" in content or "gems_assert_equal" in content:
            ok("使用 gems_assert_close/equal 进行比较")
        else:
            self.errors.append("测试文件未使用 gems_assert_close 或 gems_assert_equal")
            fail("未使用 utils.gems_assert_close 或 utils.gems_assert_equal")

        if "print(" in content:
            self.errors.append("测试文件中使用了 print()")
            fail("包含 print() — 禁止在 test case 中 print")

        if "utils.to_reference" in content:
            ok("使用 utils.to_reference 获取 golden reference")
        else:
            self.warnings.append("测试文件未使用 utils.to_reference")
            warn("未使用 utils.to_reference — 确认 golden reference 来源")

        # 私有 API 检查
        self._check_private_torch_api(content, "测试文件")

        # dtype 硬编码检查
        self._check_dtype_hardcoding(content, "测试文件",
                                     correct_const="utils.FLOAT_DTYPES")

        # gems_assert_close 禁止 rtol 参数
        if re.search(r"gems_assert_close\s*\([^)]*rtol\s*=", content, re.DOTALL):
            self.errors.append("测试文件中 gems_assert_close 不接受 rtol 参数")
            fail("gems_assert_close 使用了 rtol — 仅支持 atol")
        else:
            ok("gems_assert_close 未使用 rtol")

        # 测试函数命名规范
        bad_names = re.findall(r"def\s+(test_(?:perf|accuracy)_\w+)\s*\(", content)
        if bad_names:
            self.warnings.append(f"测试函数命名不规范: {bad_names}，应为 test_<op>")
            warn(f"函数命名: {', '.join(bad_names)} → 应为 test_<op>")
        else:
            ok("测试函数命名规范")

        # 显式 .cpu() / .to(torch.float64) 检测
        for i, line in enumerate(content.split("\n"), 1):
            code_part = line.split("#")[0]
            if "to_reference" in code_part:
                continue
            if ".cpu()" in code_part:
                self.warnings.append(f"测试文件第 {i} 行使用 .cpu() — 建议用 utils.to_reference()")
                warn(f"第 {i} 行: .cpu() → 建议用 utils.to_reference(inp, True)")
                break
            if ".to(torch.float64)" in code_part:
                self.warnings.append(f"测试文件第 {i} 行使用 .to(torch.float64)")
                warn(f"第 {i} 行: .to(torch.float64) → 建议用 utils.to_reference()")
                break

    def check_benchmark_file(self):
        section("Benchmark 文件检查")
        if not os.path.isfile(self.bench_path):
            if self.wrong_bench_path and os.path.isfile(self.wrong_bench_path):
                self.errors.append(
                    f"benchmark 文件名错误: test_{self.op_name}.py → 应为 test_{self.op_id}.py（去掉前导下划线）"
                )
                fail(f"文件名错误: test_{self.op_name}.py → 应为 test_{self.op_id}.py")
            else:
                self.errors.append(f"benchmark 文件不存在: {self.bench_path}")
                fail(f"文件不存在: benchmark/test_{self.op_id}.py")
            return

        ok(f"文件存在: benchmark/test_{self.op_id}.py")

