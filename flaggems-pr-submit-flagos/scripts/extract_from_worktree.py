def insert_full_config(op, worktree, repo, dry_run):
    section("Step 5: _FULL_CONFIG")
    wt_init = os.path.join(worktree, "src/flag_gems/__init__.py")
    repo_init = os.path.join(repo, "src/flag_gems/__init__.py")

    # Extract config entries from worktree
    with open(wt_init) as f:
        wt_content = f.read()

    # Find all config entries for this op
    op_id = get_op_id(op)
    config_entries = []
    for line in wt_content.split("\n"):
        line_stripped = line.strip()
        if line_stripped.startswith("(") and ("," in line_stripped):
            match = re.match(r'\(\s*"([^"]+)"\s*,\s*(\w+)', line_stripped)
            if match:
                aten_name, func_name = match.groups()
                base_name = aten_name.split(".")[0].lstrip("_")
                if func_name in (op, op_id) or base_name in (op, op_id):
                    config_entries.append(line_stripped.rstrip(",") + ",")

    if not config_entries:
        fatal(f"worktree _FULL_CONFIG 中未找到 {op} 的条目")

    for entry in config_entries:
        ok(f"配置: {entry}")

    # Read repo file
    with open(repo_init) as f:
        repo_content = f.read()

    repo_lines = repo_content.split("\n")

    # Find _FULL_CONFIG block
    config_start = None
    config_end = None
    for i, line in enumerate(repo_lines):
        if "_FULL_CONFIG" in line and "=" in line:
            config_start = i
        if config_start and line.strip() == ")":
            config_end = i
            break

    if not config_start or not config_end:
        fatal("未找到 _FULL_CONFIG 定义")

    # Insert each entry
    for entry in config_entries:
        match = re.match(r'\(\s*"([^"]+)"', entry)
        if not match:
            continue
        aten_name = match.group(1)

        # Check if already exists
        if f'"{aten_name}"' in repo_content:
            ok(f'_FULL_CONFIG 已包含 "{aten_name}"')
            continue

        # Find insertion point by alphabetical order of aten name
        insert_idx = config_end
        for i in range(config_start + 1, config_end):
            line_match = re.search(r'\(\s*"([^"]+)"', repo_lines[i])
            if line_match:
                existing_name = line_match.group(1)
                if aten_name < existing_name:
                    insert_idx = i
                    break
                insert_idx = i + 1

        indent = "    "
        repo_lines.insert(insert_idx, f"{indent}{entry}")
        config_end += 1
        ok(f'_FULL_CONFIG 插入 "{aten_name}" 在行 {insert_idx + 1}')

    new_content = "\n".join(repo_lines)

    if dry_run:
        ok("[DRY] __init__.py 将被更新")
    else:
        with open(repo_init, "w") as f:
            f.write(new_content)
        ok("__init__.py 已更新")


