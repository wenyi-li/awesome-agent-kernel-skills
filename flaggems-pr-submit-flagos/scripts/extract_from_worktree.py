def insert_operators_yaml(op, worktree, repo, dry_run):
    section("Step 6: operators.yaml")
    op_id = get_op_id(op)
    repo_yaml = os.path.join(repo, "conf/operators.yaml")
    wt_yaml = os.path.join(worktree, "conf/operators.yaml")

    with open(repo_yaml) as f:
        repo_data = yaml.safe_load(f)

    # Check if already exists
    existing_ids = [o.get("id") for o in repo_data.get("ops", [])]
    if op_id in existing_ids:
        ok(f"operators.yaml 已包含 id: {op_id}")
        # Still check inplace split below
    else:
        # Try to get entry from worktree yaml
        entry = None
        with open(wt_yaml) as f:
            wt_data = yaml.safe_load(f)
        for wt_op in wt_data.get("ops", []):
            wt_name = wt_op.get("name", wt_op.get("id", ""))
            if wt_name in (op, op_id):
                entry = dict(wt_op)
                if "name" in entry:
                    entry["id"] = op_id
                    del entry["name"]
                break

        if not entry:
            entry = {
                "id": op_id,
                "description": f"Triton kernel implementation for {op}.\n",
                "for": [op],
                "labels": ["aten", "KernelGen"],
                "kind": ["Math"],
                "stages": [{"alpha": "5.1"}],
            }
            warn(f"worktree yaml 无此算子，使用默认条目（description 需人工补充）")
        else:
            labels = entry.get("labels", [])
            if "KernelGen" not in labels:
                labels.append("KernelGen")
                entry["labels"] = labels
            entry["id"] = op_id
            stages = entry.get("stages", {})
            if isinstance(stages, dict):
                entry["stages"] = [{k: v} for k, v in stages.items()]
            ok(f"从 worktree yaml 提取条目")

        # Split: if entry's 'for' list contains both op and op_ (inplace), split them
        for_list = entry.get("for", [])
        inplace_items = [f for f in for_list if f.rstrip(".").endswith("_") and f.rstrip("_.") == op.rstrip("_")]
        non_inplace_items = [f for f in for_list if f not in inplace_items]

        if inplace_items and non_inplace_items:
            warn(f"检测到 for 列表同时包含 inplace 和非 inplace: {for_list}，自动拆分")
            entry["for"] = non_inplace_items
            _insert_yaml_entry(entry, repo_yaml, op_id, existing_ids, dry_run)
            # Create separate inplace entry
            inplace_entry = dict(entry)
            inplace_id = op_id + "_"
            inplace_entry["id"] = inplace_id
            inplace_entry["description"] = f"The in-place version of `{op_id}()`."
            inplace_entry["for"] = inplace_items
            if inplace_id not in existing_ids:
                _insert_yaml_entry(inplace_entry, repo_yaml, inplace_id, existing_ids, dry_run)
                ok(f"已拆分 inplace 变体为独立条目: id={inplace_id}")
        else:
            _insert_yaml_entry(entry, repo_yaml, op_id, existing_ids, dry_run)

    # Check _FULL_CONFIG for inplace variant not in yaml
    wt_init = os.path.join(worktree, "src/flag_gems/__init__.py")
    if os.path.isfile(wt_init):
        with open(wt_init) as f:
            wt_content = f.read()
        inplace_op = op + "_" if not op.endswith("_") else None
        if inplace_op:
            inplace_id = get_op_id(inplace_op)
            inplace_pattern = rf'\(\s*"[^"]*{re.escape(inplace_op)}[^"]*"\s*,'
            if re.search(inplace_pattern, wt_content):
                # Reload to check current state
                with open(repo_yaml) as f:
                    current_data = yaml.safe_load(f)
                current_ids = [o.get("id") for o in current_data.get("ops", [])]
                if inplace_id not in current_ids:
                    inplace_entry = {
                        "id": inplace_id,
                        "description": f"The in-place version of `{op_id}()`.",
                        "for": [inplace_op],
                        "labels": ["aten", "KernelGen"],
                        "kind": ["Math"],
                        "stages": [{"alpha": "5.1"}],
                    }
                    _insert_yaml_entry(inplace_entry, repo_yaml, inplace_id, current_ids, dry_run)
                    ok(f"_FULL_CONFIG 注册了 inplace 变体，已生成独立 yaml 条目: {inplace_id}")


def _insert_yaml_entry(entry, repo_yaml, entry_id, existing_ids, dry_run):
    """Insert a single yaml entry in alphabetical order."""
    if entry_id in existing_ids:
        ok(f"yaml 已包含 id: {entry_id}，跳过")
        return

    with open(repo_yaml) as f:
        yaml_lines = f.readlines()

    insert_line = len(yaml_lines)
    i = 0
    while i < len(yaml_lines):
        match = re.match(r"\s*-\s*id:\s*(\S+)", yaml_lines[i])
        if match:
            existing_id = match.group(1)
            if entry_id < existing_id:
                insert_line = i
                break
        i += 1

    new_lines = []
    new_lines.append(f"  - id: {entry['id']}\n")
    desc = entry.get("description", "").strip()
    if "\n" in desc or len(desc) > 60:
        new_lines.append("    description: |\n")
        for dline in desc.split("\n"):
            new_lines.append(f"      {dline.strip()}\n")
    else:
        new_lines.append(f"    description: {desc}\n")
    new_lines.append("    for:\n")
    for item in entry.get("for", []):
        new_lines.append(f"      - {item}\n")
    new_lines.append("    labels:\n")
    for label in entry.get("labels", ["aten", "KernelGen"]):
        new_lines.append(f"      - {label}\n")
    new_lines.append("    kind:\n")
    for kind in entry.get("kind", ["Math"]):
        new_lines.append(f"      - {kind}\n")
    new_lines.append("    stages:\n")
    stages = entry.get("stages", [{"alpha": "5.1"}])
    if isinstance(stages, list):
        for s in stages:
            if isinstance(s, dict):
                for k, v in s.items():
                    new_lines.append(f"      - {k}: '{v}'\n")
    elif isinstance(stages, dict):
        for k, v in stages.items():
            new_lines.append(f"      - {k}: '{v}'\n")

    yaml_lines[insert_line:insert_line] = new_lines
    ok(f"yaml 插入 id: {entry_id} 在行 {insert_line + 1}")

    if dry_run:
        ok(f"[DRY] operators.yaml 将被更新 ({entry_id})")
    else:
        with open(repo_yaml, "w") as f:
            f.writelines(yaml_lines)
        ok(f"operators.yaml 已更新 ({entry_id})")


